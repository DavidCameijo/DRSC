import pandas as pd
import matplotlib.pyplot as plt
import os
from results.ex2.ex2 import main # Import your modified main function

# Settings
sim_time = 1000
seed = 42
distributions = ["deterministic", "exponential", "uniform"]
results_folder = "."

if not os.path.exists(results_folder):
    os.makedirs(results_folder)

# 1. Run Simulations
for d_type in distributions:
    print(f"Running simulation with {d_type} distribution...")
    main(seed, sim_time, num_apps=3, run=0, folder=results_folder, dist_type=d_type)

# 2. Analyze and Plot
plt.figure(figsize=(12, 7))

for d_type in distributions:
    # Find the trace file generated by YAFS
    # Note: YAFS naming convention depends on the path passed to Sim()
    file_path = f"{results_folder}/sim_trace_ex2_3_seed_{seed}_run_{0}.csv"
    
    # We rename files manually or handle them here to avoid overwriting
    # For this script, assume you renamed them to include the dist_type
    # (Or modify ex2_3.py to include dist_type in the filename)
    
    df = pd.read_csv(file_path) # You should run and capture each separately
    df['latency'] = df['time_reception'] - df['time_emit']
    
    # Plotting mean latency over time for App_0 as an example
    app0 = df[df['app'] == 'App_0'].sort_values('time_reception')
    plt.plot(app0['time_reception'], app0['latency'], label=f"App_0 ({d_type})", alpha=0.7)

plt.axvline(x=50, color='black', linestyle='--', alpha=0.3) # Reference line
plt.title("Activity 2: Latency Comparison across Traffic Generators")
plt.xlabel("Simulation Time (s)")
plt.ylabel("Latency (ms)")
plt.legend()
plt.grid(True, alpha=0.3)
plt.savefig("activity2_comparison.png")
plt.show()